package com.example.practica_3;

public class Almacen {
    String clave;
    String nombre;
    String ubicacion;
    int cantidad;

    public Almacen(String clave, String nombre, String ubicacion, int cantidad) {
        this.clave = clave;
        this.nombre = nombre;
        this.ubicacion = ubicacion;
        this.cantidad = cantidad;
    }
}
